# 算术运算
# 加 +
# 减 -
# 乘 *
# 除 /
# 地板除 - 取整 //
# 取余   %
# 乘方   **
print(2+2)
print(2-2)
print(2*2)
print(2/2)
print(7//3)
print(7%3)
print(4**2)

# 比较运算
# !=
# >
# <
# >=
# <=
print(3 == 4)
print(3 == 3)
# 返回对的就是True，False就是错误
print(3 != 3)
print(3 > 2)
print(3 < 3)
print(4 >= 4)
print(4 <= 5)



